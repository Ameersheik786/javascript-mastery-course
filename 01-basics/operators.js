/* Operator */

/* 
    1. Arithmetic Operator
    2. Assignment Operator
    3. Comparison Operator
    4. Logical Operator
    5. String Operator
    6. Other Operator
*/

/* Arithmetic Operator */

let a = 10;
let b = 3;

console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.3333333333333335
console.log(a % b); // 1
console.log(a ** b); // 1000

/* Assignment Operator */

let c = 10;
c += 5; // 15 c = 10 + 5
c -= 5; // 10 c = 15 - 5
c *= 5; // 50 c = 10 * 5
c /= 5; // 10 c = 50 / 5
c %= 5; // 0 c = 10 % 5
c **= 5; // 0 c = 0 ** 5

/* Comparison Operator */

let d = 10;
let e = 5;

console.log(d == e); // false
console.log(d != e); // true
console.log(d > e); // true
console.log(d < e); // false
console.log(d >= e); // true
console.log(d <= e); // false

/* Logical Operator */

let f = true;
let g = false;

console.log(f && g); // false
console.log(f || g); // true
console.log(!f); // false

/* String Operator */

let h = "Hello";
let i = "World";

console.log(h + " " + i); // Hello World

/* Other Operator */

let j = 10;
let k = "10";

console.log(j == k); // true
console.log(j === k); // false

/*
    == : compare value
    === : compare value and type
*/


