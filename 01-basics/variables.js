/* Variables are used to store data in a program. */

//type of variables:
/* 
- let: is used to declare a variable that can be reassigned.
- const: is used to declare a variable that cannot be reassigned.
- var: is used to declare a variable that can be reassigned.

*/

//example of let:
let age = 20; //declaring a variable with the value 20
age = 21; //reassigning the value of the variable to 21

//example of const:
const personName = "John"; //declaring a variable with the value "John"
///const personName = "Jane"; //redeclaring the variable with the value "Jane"
// personName = "Jane"; //reassigning the value of the variable to "Jane"

//example of var:
var city = "New York"; //declaring a variable with the value "New York"
var city = "Mumbai"; //redeclaring the variable with the value "Los Angeles"
city = "Los Angeles"; //reassigning the value of the variable to "Los Angeles"

